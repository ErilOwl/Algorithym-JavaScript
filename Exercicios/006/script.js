// estrutura de decisão
// switch/case/break/default
let fruit = "orange";

switch (fruit) {
    case "orange":
    case "watermelon":
    case "mango":
    case "passionfruit":
    case "guaraná":
    case "cashew":
        console.log("Juice");
        break;
    default:
        console.log("Hey! That can't be used!");
}